function parse(docText) {
  // Rather than parsing one big block, simulate the WordImportModal iterating over blocks
  // In Word, each line here would be a block (a paragraph)
  const blocks = docText.split('\n').filter(l => l.trim().length > 0);

  const questions = [];
  let currentQuestion = null;

  for (const blockText of blocks) {
    // Only split at an option marker inside the text, OR a question marker at the very BEGINNING.
    // We can just split AT the boundary of an option ` a) `, OR the start of a block question.
    // Wait, the way `split` works:
    // If we only want `\d+[.)]` to match AT THE START of the whole block:
    // We shouldn't use `split` for the question number!
    
    // Check if the block starts with a question marker
    const blockQMatch = blockText.match(/^(?:%%LI%%\s*|(\d+)[.)]\s*)([\s\S]*)/);
    
    // Split the block into segments by option markers ANYWHERE in the block
    const splitRegex = /(?=\s+[a-eA-E][.)\]]\s+)/;
    
    const segments = blockText.split(splitRegex).map(s => s.trim()).filter(s => s.length > 0);

    for (let i = 0; i < segments.length; i++) {
        const segment = segments[i];
        
        // Is this the first segment of the block, AND it matches a question marker?
        if (i === 0 && blockQMatch && !segment.match(/^[a-eA-E][.)\]]\s+/)) {
           // It's a new question!
           if (currentQuestion) questions.push(currentQuestion);
           
           // It's possible the segment has the number in it still.
           // e.g. "12. Solve for x"
           const numMatch = segment.match(/^(?:%%LI%%\s*|(\d+)[.)]\s*)([\s\S]*)/);
           const qNum = numMatch ? numMatch[1] : '?';
           const qText = numMatch ? numMatch[2] : segment;
           
           currentQuestion = { num: qNum, text: qText, answers: [] };
        } 
        // Or is it an option?
        else {
           const optionMatch = segment.match(/^([a-eA-E])[.)\]]\s*([\s\S]*)/);
           if (optionMatch && currentQuestion) {
               let ansText = optionMatch[2].trim();
               let isCorrect = false;
               if (ansText.endsWith('*')) {
                   isCorrect = true;
                   ansText = ansText.replace(/\*+$/, '').trim();
               }
               currentQuestion.answers.push({ text: ansText, isCorrect });
           } else if (currentQuestion) {
               // Append to previous answer if options exist, otherwise append to question
               if (currentQuestion.answers.length > 0) {
                   currentQuestion.answers[currentQuestion.answers.length - 1].text += ' ' + segment;
               } else {
                   currentQuestion.text += '\n' + segment;
               }
           }
        }
    }
  }
  if (currentQuestion) questions.push(currentQuestion);
  return questions;
}

